using System;
using System.Collections.Generic;
using System.IO;
using System.IO.Compression;
using System.Text;
using Workes.SaveSystem;

namespace Workes.SaveSystem.Tests;

public sealed class SerializerOutputExampleTests
{
    [Test]
    public void WriteSerializerOutputExamples()
    {
        var outputRoot = GetOutputRoot();
        if (Directory.Exists(outputRoot))
            Directory.Delete(outputRoot, recursive: true);
        Directory.CreateDirectory(outputRoot);

        WriteExampleSave(new JsonSaveSerializer(), Path.Combine(outputRoot, "json-pretty"));
        WriteExampleSave(new JsonSaveSerializer(JsonSaveFormatting.Compact), Path.Combine(outputRoot, "json-compact"));
        WriteExampleSave(
            new CompressedSaveSerializer(new JsonSaveSerializer(JsonSaveFormatting.Compact)),
            Path.Combine(outputRoot, "json-compact-gzip"));
        File.WriteAllText(
            Path.Combine(outputRoot, "README.txt"),
            "These examples are generated by SerializerOutputExampleTests.\r\n" +
            "json-pretty/example-slot/player.json is indented JSON text.\r\n" +
            "json-pretty/example-slot/metadata.json is metadata serialized through JsonSaveSerializer.\r\n" +
            "json-compact/example-slot/player.json is compact JSON text.\r\n" +
            "json-compact/example-slot/metadata.json is metadata serialized through JsonSaveSerializer.\r\n" +
            "json-compact-gzip/example-slot/player.json.gz is compact JSON compressed with GZip.\r\n" +
            "json-compact-gzip/example-slot/metadata.json.gz is metadata serialized through JsonSaveSerializer and compressed with GZip.\r\n");

        var prettyPayload = File.ReadAllText(Path.Combine(outputRoot, "json-pretty", "example-slot", "player.json"));
        var compactPayload = File.ReadAllText(Path.Combine(outputRoot, "json-compact", "example-slot", "player.json"));
        var compressedPayload = DecompressToText(Path.Combine(outputRoot, "json-compact-gzip", "example-slot", "player.json.gz"));

        Assert.That(prettyPayload.TrimStart(), Does.StartWith("{"));
        Assert.That(prettyPayload, Does.Contain(Environment.NewLine));
        Assert.That(compactPayload.TrimStart(), Does.StartWith("{"));
        Assert.That(compactPayload, Does.Not.Contain(Environment.NewLine));
        Assert.That(compressedPayload.TrimStart(), Does.StartWith("{"));
        Assert.That(compressedPayload, Does.Not.Contain(Environment.NewLine));
        Assert.That(File.Exists(Path.Combine(outputRoot, "json-pretty", "example-slot", "metadata.json")), Is.True);
        Assert.That(File.Exists(Path.Combine(outputRoot, "json-compact", "example-slot", "metadata.json")), Is.True);
        Assert.That(File.Exists(Path.Combine(outputRoot, "json-compact-gzip", "example-slot", "metadata.json.gz")), Is.True);
    }

    [Test]
    public void WriteLargeJsonCompressionComparisonExamples()
    {
        var outputRoot = GetSizeComparisonOutputRoot();
        if (Directory.Exists(outputRoot))
            Directory.Delete(outputRoot, recursive: true);
        Directory.CreateDirectory(outputRoot);

        WriteLargeExampleSave(new JsonSaveSerializer(JsonSaveFormatting.Compact), Path.Combine(outputRoot, "json-compact"));
        WriteLargeExampleSave(
            new CompressedSaveSerializer(new JsonSaveSerializer(JsonSaveFormatting.Compact)),
            Path.Combine(outputRoot, "json-compact-gzip"));

        var jsonPath = Path.Combine(outputRoot, "json-compact", "large-slot", "world.json");
        var gzipPath = Path.Combine(outputRoot, "json-compact-gzip", "large-slot", "world.json.gz");
        var jsonBytes = new FileInfo(jsonPath).Length;
        var gzipBytes = new FileInfo(gzipPath).Length;
        var percent = gzipBytes * 100.0 / jsonBytes;

        File.WriteAllText(
            Path.Combine(outputRoot, "README.txt"),
            "These files are generated by SerializerOutputExampleTests.WriteLargeJsonCompressionComparisonExamples.\r\n" +
            $"json-compact/large-slot/world.json: {jsonBytes:N0} bytes\r\n" +
            $"json-compact-gzip/large-slot/world.json.gz: {gzipBytes:N0} bytes\r\n" +
            $"Compressed file is {percent:F1}% of the compact JSON size.\r\n" +
            "The generated data is intentionally repetitive enough to resemble structured game save data.\r\n");

        Assert.That(File.Exists(jsonPath), Is.True);
        Assert.That(File.Exists(gzipPath), Is.True);
        Assert.That(gzipBytes, Is.LessThan(jsonBytes));
        Assert.That(DecompressToText(gzipPath), Is.EqualTo(File.ReadAllText(jsonPath)));
    }

    private static void WriteExampleSave(ISaveSerializer serializer, string saveRoot)
    {
        var manager = new SaveManager<string>(
            SaveSystemOptions.Create(
                saveRootPath: saveRoot,
                serializer: serializer));
        manager.RegisterProvider(new PlayerProvider());
        manager.ValidateRegistrations();
        manager.SaveToDisk("example-slot");
    }

    private static void WriteLargeExampleSave(ISaveSerializer serializer, string saveRoot)
    {
        var manager = new SaveManager<string>(
            SaveSystemOptions.Create(
                saveRootPath: saveRoot,
                serializer: serializer));
        manager.RegisterProvider(new LargeWorldProvider());
        manager.ValidateRegistrations();
        manager.SaveToDisk("large-slot");
    }

    private static string GetOutputRoot()
    {
        var testProjectRoot = Path.GetFullPath(Path.Combine(TestContext.CurrentContext.TestDirectory, "..", "..", ".."));
        return Path.Combine(testProjectRoot, "obj", "SerializerOutputExamples");
    }

    private static string GetSizeComparisonOutputRoot()
    {
        var testProjectRoot = Path.GetFullPath(Path.Combine(TestContext.CurrentContext.TestDirectory, "..", "..", ".."));
        return Path.Combine(testProjectRoot, "obj", "SerializerSizeComparison");
    }

    private static string DecompressToText(string path)
    {
        using var input = File.OpenRead(path);
        using var gzip = new GZipStream(input, CompressionMode.Decompress);
        using var output = new MemoryStream();
        gzip.CopyTo(output);
        return Encoding.UTF8.GetString(output.ToArray());
    }

    public sealed class PlayerState
    {
        public string Name { get; set; } = "Rook";

        public int Level { get; set; } = 4;

        public bool HasKey { get; set; } = true;
    }

    private sealed class PlayerProvider : ISaveProvider<PlayerState>
    {
        public string SaveKey => "player";

        public int SchemaVersion => 1;

        public int LoadPriority => 0;

        public PlayerState CaptureState()
        {
            return new PlayerState();
        }

        public void RestoreState(PlayerState state)
        {
        }
    }

    public sealed class LargeWorldState
    {
        public string WorldName { get; set; } = string.Empty;

        public List<LargePlayerState> Players { get; set; } = new List<LargePlayerState>();

        public List<LargeInventoryItemState> SharedStash { get; set; } = new List<LargeInventoryItemState>();

        public List<LargeQuestState> Quests { get; set; } = new List<LargeQuestState>();
    }

    public sealed class LargePlayerState
    {
        public string Id { get; set; } = string.Empty;

        public string Name { get; set; } = string.Empty;

        public int Level { get; set; }

        public float Health { get; set; }

        public float Mana { get; set; }

        public List<LargeInventoryItemState> Inventory { get; set; } = new List<LargeInventoryItemState>();

        public List<string> DiscoveredLocations { get; set; } = new List<string>();
    }

    public sealed class LargeInventoryItemState
    {
        public string ItemId { get; set; } = string.Empty;

        public string DisplayName { get; set; } = string.Empty;

        public int Quantity { get; set; }

        public int Durability { get; set; }

        public List<string> Tags { get; set; } = new List<string>();
    }

    public sealed class LargeQuestState
    {
        public string QuestId { get; set; } = string.Empty;

        public string Title { get; set; } = string.Empty;

        public bool Completed { get; set; }

        public List<string> CompletedObjectives { get; set; } = new List<string>();
    }

    private sealed class LargeWorldProvider : ISaveProvider<LargeWorldState>
    {
        public string SaveKey => "world";

        public int SchemaVersion => 1;

        public int LoadPriority => 0;

        public LargeWorldState CaptureState()
        {
            var state = new LargeWorldState
            {
                WorldName = "Compression Comparison World"
            };

            for (var playerIndex = 0; playerIndex < 24; playerIndex++)
            {
                var player = new LargePlayerState
                {
                    Id = $"player-{playerIndex:D2}",
                    Name = $"Player {playerIndex:D2}",
                    Level = 10 + playerIndex,
                    Health = 100 + playerIndex,
                    Mana = 40 + playerIndex
                };

                for (var itemIndex = 0; itemIndex < 40; itemIndex++)
                {
                    player.Inventory.Add(CreateItem(playerIndex, itemIndex));
                }

                for (var locationIndex = 0; locationIndex < 60; locationIndex++)
                {
                    player.DiscoveredLocations.Add($"region-{locationIndex / 10:D2}/location-{locationIndex:D3}");
                }

                state.Players.Add(player);
            }

            for (var stashIndex = 0; stashIndex < 220; stashIndex++)
            {
                state.SharedStash.Add(CreateItem(99, stashIndex));
            }

            for (var questIndex = 0; questIndex < 140; questIndex++)
            {
                var quest = new LargeQuestState
                {
                    QuestId = $"quest-{questIndex:D4}",
                    Title = $"Quest Chain {questIndex / 10:D2} Step {questIndex % 10:D2}",
                    Completed = questIndex % 3 == 0
                };

                for (var objectiveIndex = 0; objectiveIndex < 5; objectiveIndex++)
                {
                    quest.CompletedObjectives.Add($"objective-{questIndex:D4}-{objectiveIndex:D2}");
                }

                state.Quests.Add(quest);
            }

            return state;
        }

        public void RestoreState(LargeWorldState state)
        {
        }

        private static LargeInventoryItemState CreateItem(int ownerIndex, int itemIndex)
        {
            return new LargeInventoryItemState
            {
                ItemId = $"item-template-{itemIndex % 25:D3}",
                DisplayName = $"Generated Item {itemIndex % 25:D3}",
                Quantity = 1 + itemIndex % 12,
                Durability = 50 + itemIndex % 50,
                Tags = new List<string>
                {
                    "generated",
                    $"owner-{ownerIndex:D2}",
                    $"rarity-{itemIndex % 5}",
                    $"category-{itemIndex % 8}"
                }
            };
        }
    }
}
