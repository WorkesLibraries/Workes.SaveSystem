using System;
using System.IO;
using Workes.SaveSystem;

namespace Workes.SaveSystem.Tests;

public sealed class SerializerOutputExampleTests
{
    [Test]
    public void WriteSerializerOutputExamples()
    {
        var outputRoot = GetOutputRoot();
        if (Directory.Exists(outputRoot))
            Directory.Delete(outputRoot, recursive: true);
        Directory.CreateDirectory(outputRoot);

        WriteExampleSave(new JsonSaveSerializer(), Path.Combine(outputRoot, "json"));
        WriteExampleSave(new Base64JsonSaveSerializer(), Path.Combine(outputRoot, "base64-json"));
        File.WriteAllText(
            Path.Combine(outputRoot, "README.txt"),
            "These examples are generated by SerializerOutputExampleTests.\r\n" +
            "json/example-slot/player.json is readable JSON text.\r\n" +
            "json/example-slot/metadata.json is metadata serialized through JsonSaveSerializer.\r\n" +
            "base64-json/example-slot/player.bin is Base64 text that decodes to readable JSON.\r\n" +
            "base64-json/example-slot/metadata.bin is metadata serialized through Base64JsonSaveSerializer.\r\n" +
            "The .bin files are Base64 text because ISaveSerializer currently persists provider payloads as strings.\r\n");

        var jsonPayload = File.ReadAllText(Path.Combine(outputRoot, "json", "example-slot", "player.json"));
        var base64JsonPayload = File.ReadAllText(Path.Combine(outputRoot, "base64-json", "example-slot", "player.bin"));
        var decodedBase64JsonPayload = System.Text.Encoding.UTF8.GetString(Convert.FromBase64String(base64JsonPayload));

        Assert.That(jsonPayload.TrimStart(), Does.StartWith("{"));
        Assert.That(base64JsonPayload.TrimStart(), Does.Not.StartWith("{"));
        Assert.That(decodedBase64JsonPayload.TrimStart(), Does.StartWith("{"));
        Assert.That(File.Exists(Path.Combine(outputRoot, "json", "example-slot", "metadata.json")), Is.True);
        Assert.That(File.Exists(Path.Combine(outputRoot, "base64-json", "example-slot", "metadata.bin")), Is.True);
    }

    private static void WriteExampleSave(ISaveSerializer serializer, string saveRoot)
    {
        var manager = new SaveManager<string>(
            SaveSystemOptions.Create(
                saveRootPath: saveRoot,
                serializer: serializer));
        manager.RegisterProvider(new PlayerProvider());
        manager.ValidateRegistrations();
        manager.SaveToDisk("example-slot");
    }

    private static string GetOutputRoot()
    {
        var testProjectRoot = Path.GetFullPath(Path.Combine(TestContext.CurrentContext.TestDirectory, "..", "..", ".."));
        return Path.Combine(testProjectRoot, "obj", "SerializerOutputExamples");
    }

    public sealed class PlayerState
    {
        public string Name { get; set; } = "Rook";

        public int Level { get; set; } = 4;

        public bool HasKey { get; set; } = true;
    }

    private sealed class PlayerProvider : ISaveProvider<PlayerState>
    {
        public string SaveKey => "player";

        public int SchemaVersion => 1;

        public int LoadPriority => 0;

        public PlayerState CaptureState()
        {
            return new PlayerState();
        }

        public void RestoreState(PlayerState state)
        {
        }
    }
}
